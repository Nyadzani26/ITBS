    private void JAppointmentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JAppointmentsActionPerformed
        // TODO add your handling code here:
        JTabbedTutorials.setSelectedIndex(1);
        String status = "Approved";
        ProfileClass prof = new ProfileClass();
        String StudentNo = prof.getStudentNo();

        try {
            String host = "jdbc:derby://localhost:1527/Tutorilas";
            String uName = "user1";
            String uPass = "user1";
            con = DriverManager.getConnection(host, uName, uPass);

            String query = "SELECT STUDENT_YEAR, MODULE, TUTOR_NAME, PREFERED_MODE, TIME, DATE, STUDENT_NO, STATUS FROM TUTORIAL_REQUESTS WHERE STATUS = ? AND STUDENT_NO = ?";
// Specify ResultSet.TYPE_SCROLL_INSENSITIVE and ResultSet.CONCUR_READ_ONLY for a scrollable ResultSet
            try (PreparedStatement pstmt = con.prepareStatement(query, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY)) {
                pstmt.setString(1, status);
                pstmt.setString(2, StudentNo);

                try (ResultSet rs = pstmt.executeQuery()) {
                    DefaultTableModel table = (DefaultTableModel) tRequestedSession.getModel();
                    table.setRowCount(0);
                    if (!rs.isBeforeFirst()) {
                        JOptionPane.showMessageDialog(null, "Your Tutor has not responded to your request.");
                    } else {
                        while (rs.next()) {
                            String name = rs.getString("TUTOR_NAME");
                            String studentNo = rs.getString("STUDENT_NO");
                            String module = rs.getString("MODULE");
                            String mode = rs.getString("PREFERED_MODE");
                            String time = rs.getString("TIME");
                            String date = rs.getString("DATE");
                            String dbStatus = rs.getString("STATUS");

                            String[] rowData = {name, studentNo, module, mode, time, date, dbStatus};
                            table.addRow(rowData);

                        }
                    }
                }
            } catch (SQLException err) {
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_JAppointmentsActionPerformed

