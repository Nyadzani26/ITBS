        Object time = JTime.getSelectedItem();
        Object date = JDateChooser.getDate();
        ProfileClass prof = new ProfileClass();
        String studentNo = prof.getStudentNo();
        try {
            String host = "jdbc:derby://localhost:1527/Tutorilas";
            String uName = "user1";
            String uPass = "user1";
            con = DriverManager.getConnection(host, uName, uPass);

            String query = "INSERT INTO TUTORIAL_REQUESTS (STUDENT_YEAR, MODULE, TUTOR_NAME, PREFERED_MODE, TIME, DATE, STUDENT_NO, STATUS) VALUES(?,?,?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setObject(1, studentYear);
            ps.setObject(2, module);
            ps.setObject(3, tutorName);
            ps.setObject(4, mode);
            ps.setObject(5, time);
            ps.setObject(6, date);
            ps.setString(7, studentNo);
            ps.setString(8, "Pending");

            int rowsAffected = ps.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Request successfully sent to your tutor!");
            } else {
                JOptionPane.showMessageDialog(this, "Request failed!");
            }

            // Close resources
            ps.close();
            con.close();
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
