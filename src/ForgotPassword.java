    private void btnChangePassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePassActionPerformed
        String password = tfPassword.getText();
        String student_no = tfStudentNo.getText();
        String conPass = tfConPassword.getText();
        studentLogin log = new studentLogin();
        String hashedPass = log.hashPassword(password);
        try {
            String host = "jdbc:derby://localhost:1527/Tutorilas";
            String uName = "user1";
            String uPass = "user1";
            con = DriverManager.getConnection(host, uName, uPass);

            String query = "UPDATE TUTORIALS SET PASSWOORD = ? WHERE STUDENT_NUMBER = ?";
            try (PreparedStatement pstmt = con.prepareStatement(query)) {
                pstmt.setString(1, hashedPass);
                pstmt.setString(2, student_no);

                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    if (password.length() < 8 || password.isEmpty()) {
                        JOptionPane.showMessageDialog(null, "Please the above errors");
                    } else if (!conPass.equals(password)) {
                        tfConPassword.setBorder(BorderFactory.createLineBorder(red));
                    } else {
                        tfConPassword.setBorder(BorderFactory.createLineBorder(blue));
                        JOptionPane.showMessageDialog(null, "Your passord has been updated successfully");
                        dispose();
                        log.setTitle("Login");
                        log.setVisible(true);
                    }
                }
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }


    }//GEN-LAST:event_btnChangePassActionPerformed

    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        try {
            String host = "jdbc:derby://localhost:1527/Tutorilas";
            String uName = "user1";
            String uPass = "user1";
            con = DriverManager.getConnection(host, uName, uPass);

            String StudentNo = tfStudentNo.getText();

            String query = "SELECT STUDENT_NUMBER, FIRST_NAME, LAST_NAME FROM TUTORIALS WHERE STUDENT_NUMBER = ?";
            try (PreparedStatement pstmt = con.prepareStatement(query)) {
                pstmt.setString(1, StudentNo);

                try (ResultSet rs = pstmt.executeQuery()) {
                    if (rs.next()) {
                        String firstName = rs.getString("FIRST_NAME");
                        String lastName = rs.getString("LAST_NAME");
                        String username = firstName + " " + lastName;

                        OTPGenerator email = new OTPGenerator();

                        String otp = OTPGenerator.generateOTP(4);
                        String toEmail = StudentNo + "@spu.ac.za";
                        String subject = "ITBS: OTP to change password";
                        String body = "Your OTP code is: " + otp;

                        OTPGenerator gen = new OTPGenerator();
                        gen.setOTP(otp);

                        try {
                            email.sendEmail(toEmail, subject, body);
                            JOptionPane.showConfirmDialog(null, username + ", OTP successfully sent to your Email " + toEmail);
                            JOTP.setEnabled(true);
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Failed to send email: " + e.getMessage());
                            e.printStackTrace();
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "Student Number was not found, please try again!");
                        tfPassword.setEnabled(false);
                        tfConPassword.setEnabled(false);
                    }
                }
            } catch (SQLException err) {
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_jLabel6MouseClicked

