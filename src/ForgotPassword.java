    private void tfPasswordMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tfPasswordMouseExited
        // TODO add your handling code here:
        String password = tfPassword.getText();
        if (password.isEmpty()) {
            tfPassword.setBorder(BorderFactory.createLineBorder(red, 1, false));
        } else {
            if (password.length() < 5) {
                lblPassword.setForeground(red);
                lblPassword.setText("Weak");
            } else if (password.length() < 8) {
                lblPassword.setForeground(orange);
                lblPassword.setText("Medium");
            } else if (password.length() >= 8) {
                lblPassword.setForeground(blue);
                lblPassword.setText("Strong");
            }
            tfPassword.setBorder(BorderFactory.createLineBorder(blue, 1, false));
        }
    }//GEN-LAST:event_tfPasswordMouseExited

