private void databaseConnection() {
        try {
            String host = "jdbc:derby://localhost:1527/Tutorilas";
            String uName = "user1";
            String uPass = "user1";
            con = DriverManager.getConnection(host, uName, uPass);

            String firstName = tfFirstName.getText();
            String middleName = tfMiddleName.getText();
            String lastName = tfLastName.getText();
            String studentNumber = tfStudentNo.getText();
            String password = tfPassword.getText();

            // Check if any field is empty
            if (firstName.isEmpty() || lastName.isEmpty() || studentNumber.isEmpty() || password.isEmpty()) {
                return;
            }

            // Hash the password using SHA-256
            String hashedPassword = hashPassword(password);

            
            // Prepare the SQL statement
            String query = "INSERT INTO TUTORIALS (FIRST_NAME, MIDDLE_NAME, LAST_NAME, STUDENT_NUMBER, PASSWOORD, ROLES) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, firstName);
            ps.setString(2, middleName);
            ps.setString(3, lastName);
            ps.setString(4, studentNumber);
            ps.setString(5, hashedPassword); // Store hashed password
            ps.setBoolean(6, true);
            ProfileClass prof = new ProfileClass();

            // Execute the SQL statement
            int rowsAffected = ps.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Registration successful!");
            } else {
                JOptionPane.showMessageDialog(this, "Registration failed!");
            }

            // Close resources
            ps.close();
            con.close();
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(this, err.getMessage());
        }
    }

    public String hashPassword(String password) {
        try {
            // Create MessageDigest instance for SHA-256
            MessageDigest md = MessageDigest.getInstance("SHA-256");

            // Add password bytes to digest
            md.update(password.getBytes());

            // Get the hashed bytes
            byte[] bytes = md.digest();

            // Convert bytes to hexadecimal format
            StringBuilder sb = new StringBuilder();
            for (byte b : bytes) {
                sb.append(String.format("%02x", b));
            }

            return sb.toString();
        } catch (NoSuchAlgorithmException e) {
            return null;
        }
    }
