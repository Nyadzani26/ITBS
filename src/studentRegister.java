    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        // TODO add your handling code here:
        String firstName, lastName, studentNumber, password, conPassword;

        firstName = tfFirstName.getText();
        lastName = tfLastName.getText();
        studentNumber = tfStudentNo.getText();
        password = tfPassword.getText();
        conPassword = tfConPassword.getText();

        if (firstName.isBlank()) {
            lblFirstName.setText("Required");
            tfFirstName.setBorder(BorderFactory.createLineBorder(red));
            //JOptionPane.showMessageDialog(this, "Please fill in reqired field", lastName, HEIGHT);
        } else if (lastName.isBlank()) {
            lblLastName.setText("Required");
            tfLastName.setBorder(BorderFactory.createLineBorder(red));
        } else if (studentNumber.isBlank()) {
            lblStudentNo.setText("Should be 9 Characters");
            tfStudentNo.setBorder(BorderFactory.createLineBorder(red));
        } else if (password.isBlank() || password.matches("[^a-zA-Z0-9]") || password.length() < 8) {
            tfPassword.setBorder(BorderFactory.createLineBorder(red));
        } else if (conPassword.isBlank() || (!(conPassword.equals(password)))) {
            lblConPassword.setText("Passwords don't match");
            tfConPassword.setBorder(BorderFactory.createLineBorder(red));
        } else {
            databaseConnection();
            dispose();
            studentLogin login = new studentLogin();
            login.setTitle("Login");
            login.setVisible(true);
        }
    }//GEN-LAST:event_btnSignUpActionPerformed

