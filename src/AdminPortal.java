    private void JUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JUpdateActionPerformed
        // TODO add your handling code here:
        try {
            String host = "db.url";
            String uName = "db.username";
            String uPass = "db.password";
            con = DriverManager.getConnection(host, uName, uPass);
            String studentNo = tfStudentNo.getText();

            // Update the role in the TUTORIALS table
            String updateQuery = "UPDATE TUTORIALS SET ROLES = false WHERE STUDENT_NUMBER = ?";
            try (PreparedStatement updateStmt = con.prepareStatement(updateQuery)) {
                updateStmt.setString(1, studentNo);

                int rowsAffected = updateStmt.executeUpdate(); // Execute the update
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Student role changed successfully");

                    // Retrieve email from the PROFILE table
                    String emailQuery = "SELECT EMAIL FROM PROFILE WHERE STUDENT_NO = ?";
                    try (PreparedStatement emailStmt = con.prepareStatement(emailQuery)) {
                        emailStmt.setString(1, studentNo);
                        try (ResultSet rs = emailStmt.executeQuery()) {
                            if (rs.next()) {
                                String toEmail = rs.getString("EMAIL");
                                String subject = "ITBS: Student Role Updated";
                                String body = "Your role has been updated to tutor. Login to your portal and create a tutor profile to make yourself available to students.\n";
                                body += "Thank you for using our service.";

                                // Send email
                                OTPGenerator email = new OTPGenerator();
                                email.sendEmail(toEmail, subject, body);
                            }
                        }
                    } catch (SQLException err) {
                        JOptionPane.showMessageDialog(null, "Error sending email: " + err.getMessage());
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No student found with the provided student number");
                }
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, "Database error: " + err.getMessage());
        }
    }//GEN-LAST:event_JUpdateActionPerformed

