    private void btnAppointActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAppointActionPerformed
        // TODO add your handling code here:
        jTabbedPane.setSelectedIndex(2);

        try {
            String host = "db.url";
            String uName = "db.username";
            String uPass = "db.password";
            con = DriverManager.getConnection(host, uName, uPass);

            String query = "SELECT * FROM TUTORIAL_REQUESTS";
            try (PreparedStatement pstmt = con.prepareStatement(query)) {

                try (ResultSet rs = pstmt.executeQuery()) {
                    DefaultTableModel table = (DefaultTableModel) TDisplayTutorials.getModel();
                    table.setRowCount(0);

                    while (rs.next()) {
                        String tutorName = rs.getString("TUTOR_NAME");
                        String module = rs.getString("MODULE");
                        String studentNo = rs.getString("STUDENT_NO");
                        String mode = rs.getString("PREFERED_MODE");
                        String time = rs.getString("TIME");
                        String date = rs.getString("DATE");
                        String status = rs.getString("STATUS");

                        String[] data = {tutorName, studentNo, module, mode, time, date, status};
                        table.addRow(data);
                    }
                }
            }

        } catch (SQLException err) {
            JOptionPane.showMessageDialog(this, err.getMessage());
        }
    }//GEN-LAST:event_btnAppointActionPerformed

