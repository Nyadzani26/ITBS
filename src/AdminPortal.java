    private void btnSearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearch1ActionPerformed
        // TODO add your handling code here:
        try {
            String host = "db.url";
            String uName = "db.username";
            String uPass = "db.password";
            con = DriverManager.getConnection(host, uName, uPass);

            String query = "SELECT FIRST_NAME, MIDDLE_NAME, LAST_NAME, STUDENT_NUMBER, ROLES FROM TUTORIALS";
            try (PreparedStatement pstmt = con.prepareStatement(query)) {

                try (ResultSet rs = pstmt.executeQuery()) {
                    DefaultTableModel table = (DefaultTableModel) tDisplayUsers.getModel();
                    table.setRowCount(0); // Clear existing table data

                    while (rs.next()) {
                        String firstName = rs.getString("FIRST_NAME");
                        String middleName = rs.getString("MIDDLE_NAME");
                        String lastName = rs.getString("LAST_NAME");
                        String studentNo = rs.getString("STUDENT_NUMBER");
                        boolean student = rs.getBoolean("ROLES");

                        // Add data to the table model
                        String[] rowData = {firstName, middleName, lastName, studentNo, student ? "Student" : "Tutor"};
                        table.addRow(rowData);
                    }
                }
            } catch (SQLException err) {
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_btnSearch1ActionPerformed

